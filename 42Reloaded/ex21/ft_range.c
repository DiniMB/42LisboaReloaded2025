#include <stdlib.h>

int *ft_range(int min, int max)
{
	int i;
	int *array;

	if (min >= max)
		return (NULL);

	array = malloc(sizeof(int) * (max - min));
	if (array == NULL)
		return (NULL);
	i = 0;
	while (min < max)
	{
		array[i] = min;
		min++;
		i++;
	}
	return (array);
}

